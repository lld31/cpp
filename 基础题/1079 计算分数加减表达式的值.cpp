#include <stdio.h>
/*
输入n的值，求1/1−1/2+1/3−1/4+1/5−1/6+1/7−1/8+...+(−1)^(n−1)⋅1/n的值。
【输入】输入一个正整数n。(1<=n<=1000)
【输出】输出一个实数，为表达式的值，保留到小数点后四位。
*/
int main() {
	int n = 0; 
	printf("输入n的值(1<=n<=1000)："); 
	scanf("%d", &n);
	
	double sum=0,fu=1.00;
	
	for (int i = 1; i <= n; i++) {
		sum+=fu/i;//使用指数，也可以使用符号运算
		fu*=-1;
	}
	printf("分数序列前 %d 项的和: %.4lf",n,sum);//精确到小数点后4位
	return 0;
}
